				<h5 class="subheader">Råd 8: Sørg for at tænke ekstern evaluering af metodernes virkning og samtalegruppernes samlede effekt for deltagerne ind fra starten af.</h5>
				<ul class="circle">
					<li>
						Det er umuligt at måle samtalegruppernes effekt for deltagernes udvikling, hvis man ikke har foretaget en detaljeret ’baseline-undersøgelse’ af deltagernes situation <i>før</i> deltagelse til sammenligning med deltagernes situation <i>under</i> og <i>efter</i> deltagelse i samtalegrupperne.
					</li>
					<li>
						Det kræver kontrolgrupper at kunne udpege samtalegrupperne som direkte årsag til ændringer i deltagernes liv, da deltagerne bl.a. kan modtage anden form for støtte samtidig.
					</li>
					<li>
						Evalueringen bør ikke foretages af direkte projektinvolverede, men af en ekstern evaluator der kan møde feltet mere objektivt.

					</li>
				</ul>

				<h5 class="subheader">Råd 9: Indled et samarbejde med relevante aktører, der vil kunne henvise både potentielle deltagere og gruppeledere til samtalegrupperne.</h5>
				<ul class="circle">
					<li>Relevante aktører for målgruppen kan fx være læger, psykologer eller folkeskolelærere, og de bør opsøges tidligt i forløbet for at være med til at udtænke en rekrutterings- og kommunikationsstrategi.</li>
				</ul>

				<h5 class="subheader">Råd 10: Udform informationsmateriale efter målgruppens karakteristika og i samarbejde med relevante aktører.</h5>
				<ul class="circle">
					<li>Sammen med de nye samarbejdspartnere udtænkes en varieret række af informationsmateriale, der passer til målgruppen.</li>
					<li>Informationsmateriale kan være alt fra brochurer til hjemmesider eller korte film.</li>
				</ul>

				<h5 class="subheader">Råd 11: Opstil minimumskrav til deltagerne i samtalegrupperne.</h5>
				<ul class="circle">
					<li>Der er gode erfaringer med at opstille krav til deltagerne i samtalegrupperne i form af fx, at: Overholde tavshedspligten, kunne både lytte og tale, komme til tiden og melde afbud hvis man er forhindret, møde regelmæssigt gennem mindst et halvt år og være uafhængige af alkohol og stoffer.</li>
					<li>Ved et første møde med deltagerne kan det vurderes, om de er egnede til at deltage i en samtalegruppe.</li>
				</ul>

				<h5 class="subheader">Råd 12: Opstil krav til gruppelederne af samtalegrupperne.</h5>
				<ul class="circle">
					<li>Kravene til de frivillige gruppeledere kan fx være: At være fyldt 26 år, et tidsforbrug på ca. 3 timer om ugen plus tid til supervision/kurser m.m., at have underskrevet erklæring om tavshedspligt, at få indhentet udvidet straffeattest, at deltage i supervisionsmøder og overholde de nedskrevne regler og rammer for gruppelederarbejdet.</li>
					<li>Ved et første møde med ansøgere kan det på baggrund af disse kriterier vurderes, om de er egnede til at lede en samtalegruppe.</li>
				</ul>
