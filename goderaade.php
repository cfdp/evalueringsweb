			     <ul class="circle">
					<li>Det er ikke nødvendigvis godt at lægge en fælles metode ned over alle gruppeledere. Det er netop et gode ved samtalegrupperne, at grupperne selv finder deres form i fællesskab.</li>
					<li>Det anbefales alligevel at støtte gruppelederne ved at tilbyde dem et kort intro-forløb om det fælles værdisæt, stedets historie og muligheder samt en fast mødeform i forhold til løbende faglig refleksion.</li>
				</ul>

				<h5 class="subheader">Råd 14: Tilknyt gerne to gruppeledere til hver samtalegruppe, men sørg samtidig for at matche dem og at formalisere samarbejdet mellem de to.</h5>
				<ul class="circle">
					<li>Der er gode erfaringer med at være to gruppeledere knyttet til hver enkelt samtalegruppe.</li>
					<li>Da man ikke kender hinanden på forhånd, anbefales det, at ledelsen gør noget ud af matchningen på baggrund af de indledende samtaler.</li>
					<li>Det anbefales også at formalisere samarbejdet mellem de to gruppeledere i form af fx en fast ’eftersnak’ efter hvert gruppemøde, en fælles introduktion og supervision.</li>
				</ul>

				<h5 class="subheader">Råd 15: Sammensæt samtalegrupperne efter deltagernes personligheder frem for på baggrund af deres ’diagnoser’</h5>
				<ul class="circle">
				<li>Der er dårlige erfaringer med at oprette for temaopdelte grupper, fordi diagnosen eller temaet kommer til at fylde for meget og kan lægge distance til de personlige følelser.</li> 
				<li>Sammensætning af deltagere med visse diagnoser såsom spiseforstyrrelser kan få et negativt udfald for deltagerne i gruppen i og med, at sygdommen kan eskalere i samværet.</li>
				<li>Til gengæld har man oplevet at finde mange følelsesmæssige ligheder i vidt forskellige baggrunde, og at det er til stor hjælp for deltagerne.</li>
				<li>Deltagerne har følt det befriende, at de selv har kunnet vælge, hvad der skulle tales om, idet bare det at have en almindelig snak med andre kan være lindrende i sig selv.</li>
				<li>Man har derfor haft større succes med at sammensætte grupperne efter deltagernes alder, køn, personligheder og eventuelle svagheder (såsom dominans, åbenhed, generthed og fobier).</li>
			</ul>

