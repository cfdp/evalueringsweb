			<div class="content" data-section-content>
			    <h5 class="subheader">Råd 17: Sørg for en ordentlig afsked med gruppelederne samt at få forankret deres erfaringer.</h5>
				<ul class="circle">
				<li>Erfaringerne viser, at det kan være hårdt for både deltagere og den anden gruppeleder, når en (med-)gruppeleder stopper.</li>
				<li>Hvis man arbejder med frivillige gruppeledere, må man være indstillet på, at de kan ophøre deres funktion meget øjeblikkeligt.</li>
				<li>Det er derfor gavnligt at lave en form for kontrakt med gruppelederne, der forpligter dem til fx at afholde to forløbsafslutningsmøder, hvor de selv er med til at introducere den nye gruppeleder, tage ordentligt afsked med gruppen og efterlade noget skriftligt om deres erfaringer med samtalegrupperne.</li>
				</ul>

				<h5 class="subheader">Råd 18: Sørg for, at deltagerne kommer til et par møder, før de skal afslutte deres samtalegruppeforløb og indlæg her en snak om, hvad de kan tage med sig fra samtalegrupperne.</h5>
				 <ul class="circle">
				<li>For deltagerne er det vigtigt, at der ikke er nogen stram tidshorisont i samtalegrupperne. Dette er i modsætning til andre steder i behandlingssystemet.</li>
				<li>De fleste deltagere vil på et tidspunkt selv føle det naturligt at stoppe i samtalegrupperne, når de fx begynder at arbejde, studere eller føler sig for gamle.</li>
				<li>Men de kan alligevel have glæde af en langsom afskedsproces, hvor de indstiller sig på at stå på egne ben og på at tage de gode erfaringer med samtalegrupperne med sig ud i livet.</li>
				</ul>

				<h5 class="subheader">Råd 19: Sørg for en sidste dataindsamling i forhold til evaluering af samtalegruppernes effekt, når en deltager eller gruppeleder afslutter sin deltagelse.</h5>
				<ul class="circle">
					<li>
						Denne sidste dataindsamling skal sammenlignes med de første og med kontrolgrupperne for at kunne måle eventuelle udviklinger.
