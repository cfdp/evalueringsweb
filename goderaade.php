			    <h5 class="subheader">Råd 16: Sørg altid for jævnlig professionel supervision til gruppelederne.</h5>
			    <ul class="circle">
					<li>Arbejdet med udsatte mennesker vækker ofte følelser af magtesløshed og sårbarhed. Dette fænomen kaldes sekundær eller vikarierende traumatisme (Bang 2004). Man kan blive bærer af andres traumer og få den primært traumatiseredes symptomer.</li> 
					<li>Det er derfor vigtigt at få vendt tanker og bekymringer med en supervisor, så man som gruppeleder ikke møder de unge med afmagt, men med overskud og håb. </li>
					<li>Det er også i supervisionen, at man som gruppeleder kan få reflekteret over samarbejdet gruppelederne imellem samt få rum til at lufte bekymringer i forbindelse med den enkelte unges situation og fremtidige muligheder i gruppen. </li>
				</ul>
