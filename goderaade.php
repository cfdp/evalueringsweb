			<div class="content" data-section-content>
					<h5 class="subheader">Råd 1: Definer målgruppen detaljeret, men med plads til rummelighed.</h5>
					<ul class="circle">
						<li>Hvem skal jeres samtalegrupper henvende sig til?</li>
						<li>Det er vigtigt at være sig samtalegruppernes målgruppe bevidst i forhold til den øvrige planlægning; i valg af lokalitet, lokaler, samarbejdspartnere, rekrutteringsform m.m.</li>
						<li>Men en af samtalegruppernes styrker er samtidig rummeligheden overfor mange forskellige typer mennesker og historier, da det fjerner fokus fra diagnosen og skærper opmærksomheden om de fælles menneskelige følelser. </li>
						<li>Samtalegrupper kan også være et godt alternativ til det offentlige systems øvrige støttemuligheder, hvor man kan blive nægtet adgang, hvis man fx er selvmordstruet, og hvor der kan være meget fokus på ’de tunge eller bestemte diagnoser’. </li>	
					</ul>

					<h5 class="subheader">Råd 2: Sørg for en kommunal forankring.</h5>
					<ul class="circle">
						<li>Gennem et aktivt partnerskab med den lokale kommune kan man sikre sig at netværk til andre tilbud og koblingen mellem forskellige sociale indsatser styrkes. Kommunen kan på den måde være en platform for at spotte unge der kan profitere af tilbuddet i samtalegrupperne.</li>
						<li>Den økonomiske del af partnerskabet kan for eksempel bestå i at kommunen kan hjælpe med at finde egnede lokaler og være medfinansierende på de faste udgifter som for eksempel løn.</li>
						</ul>
					<h5 class="subheader">Råd 3: En landsorganisation som koordinator er et stort plus i forhold til videreformidling og implementering af erfaringerne.</h5>
					<ul class="circle">
						<li>En landsorganisation vil have lettere ved at udbrede samtalegruppernes erfaringer og budskaber end projektdeltagerne og kommunen selv.</li>
						<li>Den landsdækkende videreformidling kan samtidig give konstruktiv og udviklende feedback fra andre afdelinger i landet. </li>
						<li>Landsorganisationen kan have en stor værdi som ’kvalitetssikring’, der kan gøre det lettere at rekruttere frivillige gruppeledere og deltagere i samtalegrupperne samt at sparre de projektansvarlige.</li>  
						<li>Omvendt får landsorganisationen også sine værdier præsenteret via projektet.</li> 
					</ul>

					<h5 class="subheader">Råd 4: Det er fordelagtigt at samarbejde med andre samtalegrupper med udgangspunkt i et fælles koncept fra begyndelsen</h5>
					<ul class="circle">
						<li>Samarbejder mellem samtalegrupper forskellige steder i landet kan give inspiration og konstruktiv feedback.</li>
						<li>Et fælles koncept fra påbegyndelsen af samarbejdet med fx en fast mødestruktur på tværs samt et koordinerende team med repræsentanter fra alle parter kan være gavnligt.</li>
					</ul>

					<h5 class="subheader">Råd 5: Placer samtalegrupperne centralt og tæt på offentlige transportmidler - et sted hvor deltagerne stadig kan være anonyme, når de går ind, og med rare, hjemlige lokaler.</h5>
					<ul class="circle">
						<li>Lokaliteten har stor betydning for deltagelsen i grupperne og bør derfor være central og let tilgængelig.</li> 
						<li>Det er også vigtigt, at deltagerne ikke føler sig ’stemplet’, når de går ind til samtalegrupperne. Her kan et sted med mange forskellige aktiviteter være anbefalelsesværdigt.</li> 
						<li>Det frarådes at enten mørke afsidesliggende lokaler eller lokaler med stor synlighed for andre af hensyn til deltagerne.</li> 
						<li>Endelig betyder det meget for deltagerne, at rummene indrettes hyggeligt og hjemligt, så det ikke minder om et ’psykiatrisk hospital’ eller anonyme kommunale mødelokaler.</li>
					</ul> 

					<h5 class="subheader">Råd 6:Tidspunkterne for samtalegrupperne skal tilpasses målgruppens hverdag.</h5>
					<ul class="circle">
					<li>Hvordan ser netop jeres målgruppes hverdag ud? – Hør gerne deltagerne selv, hvilket tidspunkt på dagen, der passer dem bedst i forhold til samtalegrupperne.</li>
					<li>Samtalegrupperne for de unge har primært foregået i tidsrummet kl. 16-18 med henblik på, at deltagerne kan kombinere samtalegrupperne med skole/arbejde og samtidig have andre aktiviteter om aftenen.</li>
					</ul>

					<h5 class="subheader">Råd 7: Planlæg størrelsen på samtalegrupperne, så der er en ’gruppe’ hver gang til for trods afbud og udskiftning. Samtidig må gruppen ikke blive så stor, at der ikke er plads til, at alle får sagt noget.</h5>
					<ul class="circle">
					<li>Vær opmærksom på gruppe-støtte-mulighedens fordele frem for den individuelle behandling.</li> 
					<li>Der skal være plads til, at alle får sagt noget, at alle føler sig vigtige i forhold til de andre og til følelsen af et fællesskab.</li>
					<li>Der er gode erfaringer med 5-9 deltagere i hver gruppe.</li>
					</ul>

